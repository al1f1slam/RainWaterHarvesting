<h2>What Is Rain Water Harvesting?</h2>
<p>Rain Water harvesting is a natural gift by almighty Allah. It is also called rain water collection technology or rainwater treatment system. It is a technology that collect the water at rainy season and store it for daily human use. Rainwater treatment system is epoch-useful decision for the Bangladesh as well as worldwide. Rainwater can be source of our daily water needed.By proper treatment of collected rain water can use for drinking purpose also. The rainwater those are drinkable, can use for different purpose. Such as irrigation like gardening. Like flush at toilet or car wash. It can also use for launder clothes. And it can also use our daily life. Such as taking shower, washing hand. In dry season it can be great asset as well. So, finally we can summarize Rain Water harvesting is a natural source. And it is collected storage at rainy season from roof tops, parks, road or from open grounds. And make treatment as require.</p>

